package com.company.Homework8.Problem22;

public class DistanceConverter {

    public static double convertMilesToMeters(double mile) {
        return mile * 1609;
    }

    public static double convertMetersToMiles(double meters) {
        return meters / 1609;
    }

    public static double convertInchToCentimeter(double inch) {
        return inch * 2.54;
    }

    public static double convertCentimeterToInch(double centimeter) {
        return centimeter / 2.54;
    }
}
